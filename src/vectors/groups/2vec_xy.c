#include "../../../includes/mbx.h"

t_2vec2	t2vec2_xy(int x1, int y1, int x2, int y2)
{
	return ((t_2vec2){.p1 = vec2(x1, y1), .p2 = vec2(x2, y2)});
}

t_2vec2i	t2vec2i_xy(int x1, int y1, int x2, int y2)
{
	return ((t_2vec2i){.p1 = vec2i(x1, y1), .p2 = vec2i(x2, y2)});
}
